.P
The common DUG discusses the general way in which release areas and installations
are handled at OSF.  Please read the chapter on installation in that document
before proceeding.
.P
Since DCE has to this point only been released in the form of a Snapshot
or a Developers' Kit, information and procedures regards how to install the
DCE is not yet fixed.  Please refer to the appropriate Snapshot or Developers'
Kit Release Notes for specific information on how to install DCE.
